    // Método para cargar los datos en la tabla desde la base de datos
    private void cargarDatos() {
        List<usuario> usuarios = ConnectionDB.obtenerUsuarios(); // Obtener usuarios desde la base de datos
        DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
        
        // Limpiar cualquier dato previo
        model.setRowCount(0);

        // Recorrer la lista de usuarios y agregar las filas a la tabla
        for (usuario usuario : usuarios) {
            model.addRow(new Object[]{
                usuario.getId_usuario(),
                usuario.getNombre(),
                usuario.getCorreo(),
                usuario.getContraseña(),
                usuario.getRol()
            });
        }
    }
